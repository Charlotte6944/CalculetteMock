package fr.m2i.domain;

public class Calculatrice {

    public int add(int a, int b) {
        return a + b;
    }

    public int sous(int a, int b) {
        return a - b;
    }

    public int multiply(int a, int b) {
        return a * b;
    }

    public int div(int a, int b) {
        return a/b;
    }

}
